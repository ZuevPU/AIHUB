const METHODOLOGICAL_ERRORS = 'overfitting, data leakage, p-hacking, correlation as causation, ignoring missing data mechanism';

export function buildThreeStepPrompt(params: {
  role: string[];
  goal: string[];
  outputFormat: string[];
  depth: string[];
  domain: string[];
  visualization: string[];
  style: string[];
  technical: string[];
  negative: string[];
  advanced: string[];
  customInputs: Record<string, string>;
  audience: string;
}): string {
  const {
    role,
    goal,
    outputFormat,
    depth,
    domain,
    visualization,
    style,
    technical,
    negative,
    advanced,
    customInputs,
    audience,
  } = params;

  const primaryRole = role[0] || '';
  const roleText = primaryRole ? (ROLE_PROMPT_MAP[primaryRole] || primaryRole) : '[укажи роль]';
  const audienceText = audience.trim() || (primaryRole ? ROLE_AUDIENCE_MAP[primaryRole] : 'руководитель, методист, коллеги');
  const priorityText = primaryRole ? ROLE_PRIORITY_MAP[primaryRole] : '[ключевой приоритет]';
  const normativesText = primaryRole ? ROLE_NORMATIVES_MAP[primaryRole] || '' : '';

  const goalText = goal.length > 0 ? goal.map((g) => GOAL_PROMPT_MAP[g] || g).join('; ') : '[укажи цель]';

  const formatExport = outputFormat.length > 0
    ? outputFormat.map((f) => {
        const key = f === 'Создай интерактивный дашборд' ? 'Дашборд / Интерактивный отчёт' : f;
        return FORMAT_PROMPT_MAP[key] || f;
      }).join('; ')
    : '[укажи формат]';

  const depthData = depth[0] ? DEPTH_PROMPT_MAP[depth[0]] : null;
  const depthLevel = depthData?.level ?? 'дескриптивной статистики';
  const depthMetrics = depthData?.metrics ?? 'среднее, медиана, % изменения';
  const depthTests = depthData?.tests ?? 'χ², t-test';

  const domainText = domain.length > 0 ? domain.join('; ') : '';

  const hasTextAnalytics = advanced.includes('Текстовая аналитика (LDA, sentiment)');
  const hasGeomap = visualization.some((v) => v.includes('Геокарта'));
  const isGosudarstvo = primaryRole === 'Госуправление / ФОИВ';

  let vizText = visualization.map((v) => VISUALIZATION_PROMPT_MAP[v] || v).join('; ');
  if (hasGeomap && !vizText.includes('пространственная привязка')) {
    vizText += '; пространственная привязка данных';
  }

  const styleText = style[0] ? (STYLE_PROMPT_MAP[style[0]] || style[0]) : 'практико-ориентированный';

  const techItems = [...technical];
  if (isGosudarstvo && !techItems.some((t) => t.includes('агрегация'))) {
    techItems.push('агрегация при N<10, удаление прямых идентификаторов');
  }

  const techText = techItems.join('; ');
  const negText = negative.length > 0
    ? `--no ${negative.join(', ')}`
    : `--no ${METHODOLOGICAL_ERRORS}`;

  const domainContext = domainText ? `\nТвой фокус: ${domainText}.` : '';
  const priorityContext = priorityText ? ` Приоритет: ${priorityText}.` : '';

  const step1 = `=== ШАГ 1: ДИАГНОСТИКА ===
[РОЛЬ] Ты — ${roleText}. Фокус: ${priorityText}.${domainContext}

[ДАННЫЕ] Проанализируй загруженный файл(ы).

[ЗАДАЧА] 
1. Качество: пропуски (% по ключевым полям), дубликаты, некорректные форматы, выбросы (метод: IQR для чисел).
2. Гипотезы: предложи 3-5 гипотез для цели "${goalText}". Для каждой: как проверка поможет принять решение для ${audienceText}.`;

  const step2 = `=== ШАГ 2: ПЛАНИРОВАНИЕ ===
[РОЛЬ] Та же роль + экспертиза в ${depthLevel}.
[ВХОД] Используй гипотезы и проблемы качества из Шага 1.

[ЗАДАЧА] Составь пошаговый план анализа:

1. Предобработка: пропуски, выбросы, нормализация форматов.
2. Метрики: ${depthMetrics} — для каких полей и почему.
3. Статистика: ${depthTests} + интерпретация${normativesText ? ' в контексте ' + normativesText : ''}.
4. Сегментация (если применимо к цели): метод группировки, критерии, профили групп.
${hasTextAnalytics ? '5. Текстовая аналитика: LDA/частотный анализ, топ-тем, тональность, примеры цитат.\n6. ' : '5. '}Визуализация: ${vizText || 'таблица + 2-3 графика'}.
${hasTextAnalytics ? '7. ' : '6. '}Валидация: репрезентативность, устойчивость выводов.

[ВЫХОД] План с оценкой сложности (низкая/средняя/высокая).`;

  const formatExportStr = outputFormat.length > 0
    ? outputFormat.map((f) => {
        const key = f === 'Создай интерактивный дашборд' ? 'Дашборд / Интерактивный отчёт' : f;
        return FORMAT_PROMPT_MAP[key] || f;
      }).join('; ')
    : '[укажи формат]';

  const step3 = `=== ШАГ 3: ИСПОЛНЕНИЕ + ОТЧЁТ ===
[РОЛЬ] ${roleText} + Data Scientist.${domainContext}
[ВХОД] Строго следуй плану из Шага 2.

[КОНТЕКСТ] Приоритет: интерпретируемость > сложность. Целевое время чтения: 30 секунд. Если конфликт детализации и краткости — выбирай краткость.

[ЗАДАЧА] Отчёт в формате: ${formatExportStr}.

1. Метрики: таблица с условным форматированием (зелёный=норма, жёлтый=внимание, красный=критично) + 1-2 предложения интерпретации${normativesText ? ' в контексте ' + normativesText : ''}.
2. Инсайты (3-5): "[Наблюдение] → [Данные: метрика, значимость] → [Интерпретация]".
3. Рекомендации для ${audienceText}: приоритизация по воздействию/затратам, формат "Действие + Ответственный + Срок + Критерий успеха". Стиль: ${styleText}.
4. Ограничения: честно — что данные НЕ позволяют утверждать, какие допущения сделаны.
${hasTextAnalytics ? '5. Тексты: топ-3 тем + примеры цитат + тональность.\n6. ' : '5. '}Визуализация (3-5 элементов): ${vizText || 'тип + данные + пояснение'}. Требования: целые оси, без 3D, доступная цветовая схема.

[ФИНАЛЬНЫЕ ПРАВИЛА]
${negText};
+ ${techText || 'агрегация при N<10, удаление ПДн, коды вместо названий ОУ'};
+ приоритет краткости, все выводы в агрегированном виде.
${customInputs.negative ? `+ ${customInputs.negative}` : ''}`.trim();

  return [step1, step2, step3].join('\n\n');
}
